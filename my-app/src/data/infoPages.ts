export const infoPageList: { path: string; label: string }[] = [
  { path: '/payment', label: 'Оплата' },
  { path: '/delivery', label: 'Доставка' },
  { path: '/returns', label: 'Возврат товара' },
];

export function resolveInfoPageSlug(pathname: string, paramSlug?: string): InfoPageSlug | undefined {
  const fromPath = infoPagePaths[pathname];
  if (fromPath) return fromPath;
  if (paramSlug && paramSlug in infoPages) {
    return paramSlug as InfoPageSlug;
  }
  return undefined;
}
